type IndexQuery = {
  pageNumber: string;
};

type MessageData = {
  body: string;
  fromMe: boolean;
  read: boolean;
  quotedMsg?: Message;
  number?: string;
  closeTicket?: true;
};

export const index = async (req: Request, res: Response): Promise<Response> => {
  const { ticketId } = req.params;
  const { pageNumber } = req.query as IndexQuery;
  const { companyId, profile } = req.user;
  const queues: number[] = [];

  if (profile !== "admin") {
    const user = await User.findByPk(req.user.id, {
      include: [{ model: Queue, as: "queues" }]
    });
    user.queues.forEach(queue => {
      queues.push(queue.id);
    });
  }

  const { count, messages, ticket, hasMore } = await ListMessagesService({
    pageNumber,
    ticketId,
    companyId,
    queues
  });

  SetTicketMessagesAsRead(ticket);

  return res.json({ count, messages, ticket, hasMore });
};

export const store = async (req: Request, res: Response): Promise<Response> => {
  const { ticketId } = req.params;
  const { body, quotedMsg }: MessageData = req.body;
  const medias = req.files as Express.Multer.File[];
  const { companyId } = req.user;

  const ticket = await ShowTicketService(ticketId, companyId);
  SetTicketMessagesAsRead(ticket);

  try {
    const integration = await QueueIntegrations.findOne({
      where: { queueId: ticket.queueId, type: "typebot" }
    });

    if (integration) {
      logger.info(`Chamando Typebot para ticket ${ticket.id} com mensagem: ${body}`);
      await typebotListener({
        wbot: null,
        msg: { key: { remoteJid: `${ticket.contact.number}@c.us` }, pushName: ticket.contact.name, body } as any,
        ticket,
        typebot: integration
      });
      logger.info(`Resposta do Typebot processada com sucesso para ticket ${ticket.id}`);
    }
  } catch (error) {
    logger.error("Erro na integração com Typebot:", error);
  }

  if (medias) {
    await Promise.all(
      medias.map(async (media: Express.Multer.File, index) => {
        await SendWhatsAppMedia({ media, ticket, body: Array.isArray(body) ? body[index] : body });
      })
    );
  } else {
    await SendWhatsAppMessage({ body, ticket, quotedMsg });
  }

  return res.send();
};
